package Technical_Array;
//Q6.JAVA program to swap two numbers without using a third variable or using only two variables.

public class Swap_Two {
public static void main(String[] args) {
	int a=56;
	int b=15;
	a=a+b;
	b=a-b;
	a=a-b;
	System.out.println(a);
	System.out.println(b);
}
}
