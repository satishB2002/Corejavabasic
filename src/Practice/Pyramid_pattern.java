package Practice;

import String.intern_method;

public class Pyramid_pattern {
public static void main(String[] args) {
	
	for (int i = 1; i <=9; i++) {
		
		for ( int j = 1; j <=i; j++) {
			
				System.out.print(" ");
			
				for ( j = 1; j <=i; j++) {
					
					System.out.print("*");
					
			
		}
		System.out.println();
		
	}
	
	
}
}
}
